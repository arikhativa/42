#include <string.h>

char	*ft_strstr(char *s1, char *s2)
{
	int i;
	int z;

	if (!(*s2))
		return (s1);
	i = 0;
	z = 0;
	while (s1[i])
	{
		while (s1[i + z] == s2[z])
		{
			if (s2[z + 1] == '\0')
				return (&s1[i]);
			z++;
		}
		z = 0;
		i++;
	}
	return (NULL);
}
