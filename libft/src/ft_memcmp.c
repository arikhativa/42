int		ft_memcmp(void *s1, void *s2, unsigned int n)
{
	unsigned int i;

	i = 0;
	while (i < n)
	{
		if (((*(char*)(s1 + i))) != ((*(char*)(s2 + i))))
			return (((*(char*)(s1 + i))) -  ((*(char*)(s2 + i))));
		i++;
	}
	return (((*(char*)(s1 + i))) -  ((*(char*)(s2 + i))));
}
